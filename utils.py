def load_obj_as_mesh(context, filepath: str, frame: Optional[int] = None):
    """Import *filepath* (OBJ) into the current scene and return the object."""
    import bpy

    name = os.path.splitext(os.path.basename(filepath))[0]
    if frame is not None:
        name = f"{name}_f{frame:06d}"

    bpy.ops.wm.obj_import(filepath=filepath)
    obj = context.selected_objects[-1] if context.selected_objects else None
    if obj:
        obj.name = name
    return obj


