def load_vdb_sequence(context, vdb_dir: str, start_frame: int = 1) -> Optional[object]:
    """Import a directory of ``.vdb`` files as an animated Blender Volume object.

    Blender's native OpenVDB support is used (``bpy.ops.object.volume_import``).
    Returns the created Volume object or None if no files are found.
    """
    import bpy
    import glob as _glob

    vdb_files = sorted(_glob.glob(os.path.join(vdb_dir, "*.vdb")))
    if not vdb_files:
        log.warning("No .vdb files found in %s", vdb_dir)
        return None

    first = vdb_files[0]
    filename = os.path.basename(first)

    bpy.ops.object.volume_import(
        filepath=first,
        files=[{"name": filename}],
        directory=vdb_dir,
    )

    vol_obj = context.selected_objects[-1] if context.selected_objects else None
    if vol_obj is None:
        return None

    # Set up sequence playback
    vol = vol_obj.data
    try:
        vol.sequence_mode = "CLAMP"
        vol.frame_start = start_frame
        vol.frame_duration = len(vdb_files)
        vol.frame_offset = 0
    except AttributeError:
        pass  # Older Blender builds may not expose all sequence attributes

    return vol_obj


