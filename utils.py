# ---------------------------------------------------------------------------
# VTK legacy ASCII / binary loader (POLYDATA and UNSTRUCTURED_GRID)
# ---------------------------------------------------------------------------

class VTKFile:
    """Minimal parser for VTK legacy ASCII format files (POLYDATA / UNSTRUCTURED_GRID)."""

    def __init__(self, filepath: str):
        self.filepath = filepath
        self.points: list = []   # list of (x, y, z)
        self.cells: list = []    # list of vertex-index tuples
        self.cell_types: list = []
        self.point_data: dict = {}  # name -> flat list of floats
        self._parse()

    # ------------------------------------------------------------------
    def _parse(self):
        with open(self.filepath, "r", encoding="latin-1") as fh:
            lines = fh.read().splitlines()

        # The VTK legacy format has exactly 4 fixed header lines:
        #   Line 0: "# vtk DataFile Version X.X"
        #   Line 1: title / description (any string)
        #   Line 2: "ASCII" or "BINARY"
        #   Line 3: "DATASET <type>"
        # Data sections start at line 4.  We must NOT treat the leading
        # "#" on line 0 as a comment because it is part of the format.
        i = 4  # skip the 4 fixed header lines

        def _next_line():
            nonlocal i
            while i < len(lines):
                ln = lines[i].strip()
                i += 1
                if ln:
                    return ln
            return None

        while True:
            ln = _next_line()
            if ln is None:
                break
            parts = ln.split()
            if not parts:
                continue
            keyword = parts[0].upper()

            if keyword == "POINTS":
                n_pts = int(parts[1])
                coords = []
                while len(coords) < n_pts * 3:
                    ln2 = _next_line()
                    if ln2 is None:
                        break
                    coords.extend(float(v) for v in ln2.split())
                self.points = [
                    (coords[j], coords[j + 1], coords[j + 2])
                    for j in range(0, len(coords), 3)
                ]

            elif keyword in ("CELLS", "POLYGONS", "LINES"):
                n_cells = int(parts[1])
                for _ in range(n_cells):
                    ln2 = _next_line()
                    if ln2 is None:
                        break
                    vals = list(map(int, ln2.split()))
                    self.cells.append(tuple(vals[1:]))  # drop leading count

            elif keyword == "CELL_TYPES":
                n_ct = int(parts[1])
                ct_vals = []
                while len(ct_vals) < n_ct:
                    ln2 = _next_line()
                    if ln2 is None:
                        break
                    ct_vals.extend(int(v) for v in ln2.split())
                self.cell_types = ct_vals

            elif keyword == "POINT_DATA":
                n_pts = int(parts[1])
                while True:
                    ln2 = _next_line()
                    if ln2 is None:
                        break
                    p2 = ln2.split()
                    attr_kw = p2[0].upper()
                    if attr_kw == "SCALARS" and len(p2) >= 2:
                        attr_name = p2[1]
                        _next_line()  # LOOKUP_TABLE line
                        values = []
                        while len(values) < n_pts:
                            ln3 = _next_line()
                            if ln3 is None:
                                break
                            values.extend(float(v) for v in ln3.split())
                        self.point_data[attr_name] = values
                    elif attr_kw == "VECTORS" and len(p2) >= 2:
                        attr_name = p2[1]
                        values = []
                        while len(values) < n_pts * 3:
                            ln3 = _next_line()
                            if ln3 is None:
                                break
                            values.extend(float(v) for v in ln3.split())
                        self.point_data[attr_name] = values
                    else:
                        # Unknown / end of POINT_DATA section – step back
                        i -= 1
                        break


def load_vtk_as_mesh(context, filepath: str, frame: Optional[int] = None):
    """Parse a VTK POLYDATA file and create a Blender mesh object.

    Returns the created *bpy.types.Object* or *None* on failure.
    """
    import bpy
    import bmesh as bm_module

    try:
        vtk = VTKFile(filepath)
    except Exception as exc:
        log.error("Failed to parse VTK file %s: %s", filepath, exc)
        return None

    if not vtk.points:
        log.warning("VTK file %s has no POINTS data.", filepath)
        return None

    name = os.path.splitext(os.path.basename(filepath))[0]
    if frame is not None:
        name = f"{name}_f{frame:06d}"

    mesh = bpy.data.meshes.new(name)
    obj = bpy.data.objects.new(name, mesh)
    context.collection.objects.link(obj)

    bm = bm_module.new()
    verts = [bm.verts.new(pt) for pt in vtk.points]
    bm.verts.ensure_lookup_table()

    for cell in vtk.cells:
        if len(cell) >= 3:
            try:
                bm.faces.new([verts[ci] for ci in cell])
            except Exception:
                pass
        elif len(cell) == 2:
            try:
                bm.edges.new([verts[cell[0]], verts[cell[1]]])
            except Exception:
                pass

    bm.to_mesh(mesh)
    bm.free()
    mesh.update()

    return obj


