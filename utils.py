"""Utility helpers for the FluidX_Blender addon.

Covers:
  - FluidX3D C++ setup-file generation (mesh-aware, with collision/inflow support)
  - defines.hpp patching to enable required FluidX3D features before compilation
  - Exporting Blender objects as binary STL files for voxelization
  - Loading OBJ / PLY surface meshes produced by FluidX3D
  - Loading legacy ASCII/binary VTK files (POLYDATA, UNSTRUCTURED_GRID,
    and STRUCTURED_POINTS) as Blender mesh or volume objects
  - Converting binary VTK STRUCTURED_POINTS density fields to OpenVDB (.vdb)
"""

import os
import struct
import logging
from typing import Optional

log = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# C++ setup-file generation  (object-aware, v2)
# ---------------------------------------------------------------------------

_SETUP_TEMPLATE = """\
// FluidX3D simulation setup – generated by FluidX_Blender {version}
// Domain object: {domain_name}
// WARNING: This file is overwritten every bake. Do not edit manually.

#include "setup.hpp"

void main_setup() {{
    // ---- Domain ----
    const uint Nx = {Nx}u, Ny = {Ny}u, Nz = {Nz}u;
    const float nu = {nu}f;

#ifdef VOLUME_FORCE
    LBM lbm(Nx, Ny, Nz, nu, {fx}f, {fy}f, {fz}f);
#else
    LBM lbm(Nx, Ny, Nz, nu);
#endif // VOLUME_FORCE

    // ---- Solid collision geometry (voxelized from exported STL meshes) ----
{collision_code}
    // ---- Initialize fluid fields ----
    const ulong n_total = lbm.get_N();
    for(ulong n=0ull; n<n_total; n++) {{
        if(lbm.flags[n] != TYPE_S) {{
            lbm.rho[n] = {rho}f;
            lbm.u.x[n] = 0.0f;
            lbm.u.y[n] = 0.0f;
            lbm.u.z[n] = 0.0f;
            lbm.flags[n] = TYPE_F;
        }}
    }}

#ifdef EQUILIBRIUM_BOUNDARIES
    // ---- Inflow / outflow equilibrium boundary regions ----
{inflow_outflow_code}
#endif // EQUILIBRIUM_BOUNDARIES

    // ---- Domain-face boundary conditions ----
{boundary_code}
    lbm.flags.write_to_device();
    lbm.rho.write_to_device();
    lbm.u.write_to_device();

    // ---- Run simulation and export VTK volume data ----
    mkdir_p("{out_dir}");
    while(lbm.get_t() <= {time_steps}ull) {{
        if(lbm.get_t() % {output_every}ull == 0ull) {{
            lbm.rho.write_device_to_vtk("{out_dir}/rho");
            lbm.u.write_device_to_vtk("{out_dir}/u");
        }}
        lbm.run(1u, {time_steps}ull);
    }}
}}
"""

# Map human-readable boundary names to FluidX3D flag types
_BC_FLAG = {
    "PERIODIC":    "TYPE_P",
    "WALL":        "TYPE_S",
    "FREE_SLIP":   "TYPE_S",  # treated as no-slip wall; free-slip needs custom kernel
    "EQUILIBRIUM": "TYPE_E",
}


def _bc(name: str) -> str:
    return _BC_FLAG.get(name, "TYPE_S")


def get_domain_object(scene):
    """Return the first object in *scene* tagged as DOMAIN, or None."""
    for obj in scene.objects:
        if hasattr(obj, "fluidx") and obj.fluidx.object_type == "DOMAIN":
            return obj
    return None


def get_objects_by_type(scene, type_name: str) -> list:
    """Return all objects in *scene* tagged with the given FluidX type."""
    return [
        obj for obj in scene.objects
        if hasattr(obj, "fluidx") and obj.fluidx.object_type == type_name
    ]


def get_domain_world_bounds(domain_obj):
    """Return (min_corner, max_corner) of the domain object in world space.

    Returns two mathutils.Vector instances (or plain tuples if mathutils is
    not available in the current context).
    """
    corners = [domain_obj.matrix_world @ domain_obj.data.vertices[vi].co
               for vi in range(len(domain_obj.data.vertices))]
    if not corners:
        # Fall back to bounding-box corners
        import mathutils
        corners = [domain_obj.matrix_world @ mathutils.Vector(c)
                   for c in domain_obj.bound_box]

    min_x = min(v[0] for v in corners)
    min_y = min(v[1] for v in corners)
    min_z = min(v[2] for v in corners)
    max_x = max(v[0] for v in corners)
    max_y = max(v[1] for v in corners)
    max_z = max(v[2] for v in corners)
    return (min_x, min_y, min_z), (max_x, max_y, max_z)


def world_to_lattice(world_pos, domain_bounds, domain_res):
    """Map a world-space position to FluidX3D lattice coordinates.

    FluidX3D lattice positions are centred at the box centre and span
    roughly [-N/2, N/2] in each axis.  voxelize_stl() expects positions in
    these lattice units.
    """
    (min_x, min_y, min_z), (max_x, max_y, max_z) = domain_bounds
    Nx, Ny, Nz = domain_res

    wx = max_x - min_x or 1.0
    wy = max_y - min_y or 1.0
    wz = max_z - min_z or 1.0

    lx = ((world_pos[0] - (min_x + max_x) * 0.5) / wx) * Nx
    ly = ((world_pos[1] - (min_y + max_y) * 0.5) / wy) * Ny
    lz = ((world_pos[2] - (min_z + max_z) * 0.5) / wz) * Nz
    return lx, ly, lz


def world_size_to_lattice(world_size, domain_bounds, domain_res):
    """Map a world-space extent to the corresponding lattice-unit size."""
    (min_x, min_y, min_z), (max_x, max_y, max_z) = domain_bounds
    Nx, Ny, Nz = domain_res
    sx = (max_x - min_x) or 1.0
    sy = (max_y - min_y) or 1.0
    sz = (max_z - min_z) or 1.0
    lx = (world_size[0] / sx) * Nx
    ly = (world_size[1] / sy) * Ny
    lz = (world_size[2] / sz) * Nz
    return lx, ly, lz


def export_object_as_stl(obj, filepath: str) -> bool:
    """Export *obj* as a binary STL file to *filepath*.

    Returns True on success, False if the operation fails.
    Requires a valid Blender context (call from an operator).
    """
    import bpy

    # Deselect all, then select only the target object
    prev_active = bpy.context.view_layer.objects.active
    prev_mode = bpy.context.object.mode if bpy.context.object else "OBJECT"

    if prev_mode != "OBJECT":
        bpy.ops.object.mode_set(mode="OBJECT")

    bpy.ops.object.select_all(action="DESELECT")
    obj.select_set(True)
    bpy.context.view_layer.objects.active = obj

    try:
        bpy.ops.wm.stl_export(
            filepath=filepath,
            export_selected_objects=True,
            apply_modifiers=True,
            ascii_format=False,  # binary STL (required by FluidX3D)
        )
        success = True
    except Exception as exc:
        log.error("STL export failed for %s: %s", obj.name, exc)
        success = False

    # Restore previous state
    bpy.ops.object.select_all(action="DESELECT")
    bpy.context.view_layer.objects.active = prev_active
    return success


def generate_bake_setup_code(props, scene, out_dir: str, stl_dir: str) -> str:
    """Generate a FluidX3D setup.cpp that embeds scene objects.

    *props*    – scene.fluidx (FluidXProperties)
    *scene*    – bpy.types.Scene
    *out_dir*  – absolute path where VTK output will be written (forward
                  slashes, embedded in the C++ string literal)
    *stl_dir*  – absolute path where exported STL files are stored
    """
    from . import bl_info  # noqa: F401 – for version string
    version = "0.2"

    Nx, Ny, Nz = props.domain_res_x, props.domain_res_y, props.domain_res_z
    domain_res = (Nx, Ny, Nz)
    domain_name = "N/A"

    # --- Domain bounds ---
    domain_obj = get_domain_object(scene)
    if domain_obj:
        domain_name = domain_obj.name
        try:
            domain_bounds = get_domain_world_bounds(domain_obj)
        except Exception:
            domain_bounds = ((-Nx / 2, -Ny / 2, -Nz / 2), (Nx / 2, Ny / 2, Nz / 2))
    else:
        domain_bounds = ((-Nx / 2, -Ny / 2, -Nz / 2), (Nx / 2, Ny / 2, Nz / 2))

    # --- Collision geometry ---
    collision_lines = []
    for obj in get_objects_by_type(scene, "COLLISION"):
        stl_path = os.path.join(stl_dir, f"collision_{_safe_name(obj.name)}.stl")
        # Compute world bounding box for this object
        try:
            import mathutils
            bb_world = [obj.matrix_world @ mathutils.Vector(c) for c in obj.bound_box]
        except Exception:
            bb_world = []

        if bb_world:
            obj_min = tuple(min(v[i] for v in bb_world) for i in range(3))
            obj_max = tuple(max(v[i] for v in bb_world) for i in range(3))
            obj_center_w = tuple((obj_min[i] + obj_max[i]) * 0.5 for i in range(3))
            obj_size_w = tuple(obj_max[i] - obj_min[i] for i in range(3))

            lc = world_to_lattice(obj_center_w, domain_bounds, domain_res)
            ls = world_size_to_lattice(obj_size_w, domain_bounds, domain_res)
            lsize = max(ls)  # voxelize_stl uses a single float for size
            stl_fwd = stl_path.replace("\\", "/")
            collision_lines.append(
                f'    lbm.voxelize_stl("{stl_fwd}", '
                f'float3({lc[0]:.4f}f, {lc[1]:.4f}f, {lc[2]:.4f}f), '
                f'{lsize:.4f}f, TYPE_S);  // {obj.name}'
            )
        else:
            stl_fwd = stl_path.replace("\\", "/")
            collision_lines.append(
                f'    lbm.voxelize_stl("{stl_fwd}", TYPE_S);  // {obj.name} (auto-center)'
            )

    collision_code = "\n".join(collision_lines) if collision_lines else \
        "    // (no collision objects)"

    # --- Inflow / outflow ---
    inflow_outflow_lines = []
    for obj in get_objects_by_type(scene, "INFLOW"):
        try:
            import mathutils
            bb_world = [obj.matrix_world @ mathutils.Vector(c) for c in obj.bound_box]
        except Exception:
            bb_world = []
        if not bb_world:
            continue
        obj_min = tuple(min(v[i] for v in bb_world) for i in range(3))
        obj_max = tuple(max(v[i] for v in bb_world) for i in range(3))
        # Lattice index range (clamp to [0, N-1])
        lmin = [max(0, int(world_to_lattice(obj_min, domain_bounds, domain_res)[i] + Nk / 2))
                for i, Nk in enumerate([Nx, Ny, Nz])]
        lmax = [min(Nk - 1, int(world_to_lattice(obj_max, domain_bounds, domain_res)[i] + Nk / 2))
                for i, Nk in enumerate([Nx, Ny, Nz])]
        vx, vy, vz = obj.fluidx.inflow_velocity
        rho_in = obj.fluidx.inflow_density
        inflow_outflow_lines.append(
            f"    // Inflow: {obj.name}"
            f"\n    for(uint z={lmin[2]}u; z<={lmax[2]}u; z++) {{"
            f"\n        for(uint y={lmin[1]}u; y<={lmax[1]}u; y++) {{"
            f"\n            for(uint x={lmin[0]}u; x<={lmax[0]}u; x++) {{"
            f"\n                const ulong n = lbm.index(x, y, z);"
            f"\n                lbm.flags[n] = TYPE_E;"
            f"\n                lbm.rho[n] = {rho_in:.4f}f;"
            f"\n                lbm.u.x[n] = {vx:.6f}f;"
            f"\n                lbm.u.y[n] = {vy:.6f}f;"
            f"\n                lbm.u.z[n] = {vz:.6f}f;"
            f"\n            }}"
            f"\n        }}"
            f"\n    }}"
        )

    for obj in get_objects_by_type(scene, "OUTFLOW"):
        try:
            import mathutils
            bb_world = [obj.matrix_world @ mathutils.Vector(c) for c in obj.bound_box]
        except Exception:
            bb_world = []
        if not bb_world:
            continue
        obj_min = tuple(min(v[i] for v in bb_world) for i in range(3))
        obj_max = tuple(max(v[i] for v in bb_world) for i in range(3))
        lmin = [max(0, int(world_to_lattice(obj_min, domain_bounds, domain_res)[i] + Nk / 2))
                for i, Nk in enumerate([Nx, Ny, Nz])]
        lmax = [min(Nk - 1, int(world_to_lattice(obj_max, domain_bounds, domain_res)[i] + Nk / 2))
                for i, Nk in enumerate([Nx, Ny, Nz])]
        inflow_outflow_lines.append(
            f"    // Outflow: {obj.name}"
            f"\n    for(uint z={lmin[2]}u; z<={lmax[2]}u; z++) {{"
            f"\n        for(uint y={lmin[1]}u; y<={lmax[1]}u; y++) {{"
            f"\n            for(uint x={lmin[0]}u; x<={lmax[0]}u; x++) {{"
            f"\n                lbm.flags[lbm.index(x, y, z)] = TYPE_E;"
            f"\n            }}"
            f"\n        }}"
            f"\n    }}"
        )

    inflow_outflow_code = "\n".join(inflow_outflow_lines) if inflow_outflow_lines else \
        "    // (no inflow/outflow objects)"

    # --- Domain-face boundary conditions ---
    bc_lines = []
    face_cfg = [
        ("X-", "x==0u",        props.bc_x_neg),
        ("X+", "x==Nx-1u",     props.bc_x_pos),
        ("Y-", "y==0u",        props.bc_y_neg),
        ("Y+", "y==Ny-1u",     props.bc_y_pos),
        ("Z-", "z==0u",        props.bc_z_neg),
        ("Z+", "z==Nz-1u",     props.bc_z_pos),
    ]
    non_wall = [(label, cond, bc) for label, cond, bc in face_cfg if bc != "WALL"]
    if non_wall:
        bc_lines.append("    const uint Nx=lbm.get_Nx(), Ny=lbm.get_Ny(), Nz=lbm.get_Nz();")
        bc_lines.append(
            "    for(ulong n=0ull; n<lbm.get_N(); n++) {"
            " uint x=0u, y=0u, z=0u; lbm.coordinates(n, x, y, z);"
        )
        for label, cond, bc in non_wall:
            bc_lines.append(f"        if({cond}) lbm.flags[n] = {_bc(bc)};  // {label}")
        bc_lines.append("    }")
    boundary_code = "\n".join(bc_lines) if bc_lines else "    // (all faces are solid walls by default)"

    out_fwd = out_dir.replace("\\", "/")
    fx, fy, fz = props.gravity

    return _SETUP_TEMPLATE.format(
        version=version,
        domain_name=domain_name,
        Nx=Nx, Ny=Ny, Nz=Nz,
        nu=props.viscosity,
        fx=fx, fy=fy, fz=fz,
        rho=props.density,
        collision_code=collision_code,
        inflow_outflow_code=inflow_outflow_code,
        boundary_code=boundary_code,
        out_dir=out_fwd,
        time_steps=props.time_steps,
        output_every=props.output_every,
    )


def _safe_name(name: str) -> str:
    """Convert a Blender object name to a filesystem-safe string."""
    return "".join(c if c.isalnum() or c in "-_." else "_" for c in name)


def write_bake_setup_file(props, scene, fluidx_src_dir: str, out_dir: str, stl_dir: str) -> str:
    """Write the generated setup.cpp into *fluidx_src_dir*/setup.cpp."""
    os.makedirs(fluidx_src_dir, exist_ok=True)
    code = generate_bake_setup_code(props, scene, out_dir, stl_dir)
    path = os.path.join(fluidx_src_dir, "setup.cpp")
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(code)
    return path


