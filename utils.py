def patch_defines_hpp(fluidx_src_dir: str, gravity_enabled: bool = False) -> None:
    """Patch FluidX3D's defines.hpp to enable the features required by a bake.

    Changes made:
    * ``#define BENCHMARK``         → commented out  (benchmark disables everything)
    * ``EQUILIBRIUM_BOUNDARIES``    → uncommented    (for inflow/outflow TYPE_E cells)
    * ``VOLUME_FORCE``              → uncommented if *gravity_enabled* is True

    All other settings are left unchanged.
    """
    path = os.path.join(fluidx_src_dir, "defines.hpp")
    if not os.path.isfile(path):
        raise FileNotFoundError(f"defines.hpp not found at {path}")

    with open(path, "r", encoding="utf-8") as fh:
        content = fh.read()

    # Disable BENCHMARK (it undef's everything and runs a benchmark setup)
    content = _set_define(content, "BENCHMARK", enable=False)

    # Enable EQUILIBRIUM_BOUNDARIES (needed for TYPE_E inflow/outflow)
    content = _set_define(content, "EQUILIBRIUM_BOUNDARIES", enable=True)

    # Enable VOLUME_FORCE only when gravity is non-zero
    content = _set_define(content, "VOLUME_FORCE", enable=gravity_enabled)

    with open(path, "w", encoding="utf-8") as fh:
        fh.write(content)


def _set_define(content: str, name: str, enable: bool) -> str:
    """Enable or disable a ``#define NAME`` line in *content*."""
    import re
    # Match both "#define NAME" and "//#define NAME" variants
    pattern = re.compile(r"^(//\s*)?#define\s+" + re.escape(name) + r"(\s.*)?$", re.MULTILINE)
    if enable:
        replacement = f"#define {name}"
    else:
        replacement = f"//#define {name}"

    if pattern.search(content):
        content = pattern.sub(replacement, content)
    else:
        # Append if not found
        content += f"\n{replacement}\n"
    return content


