def collect_sequence_files(directory: str, extension: str) -> list:
    """Return sorted list of files with *extension* in *directory*."""
    if not os.path.isdir(directory):
        return []
    files = [
        os.path.join(directory, f)
        for f in sorted(os.listdir(directory))
        if f.lower().endswith(extension.lower())
    ]
    return files


def load_frame_sequence(context, directory: str, file_format: str, start_frame: int = 1):
    """Load all files of *file_format* from *directory* as individual mesh objects.

    Each mesh is hidden on all frames except its own, creating a simple
    frame-by-frame animation.

    Returns a list of created objects.
    """
    import bpy

    ext_map = {
        "OBJ": ".obj",
        "PLY": ".ply",
        "VTK": ".vtk",
    }
    ext = ext_map.get(file_format.upper(), ".vtk")
    files = collect_sequence_files(directory, ext)
    objects = []

    for idx, fp in enumerate(files):
        frame_num = start_frame + idx
        if file_format.upper() == "OBJ":
            obj = load_obj_as_mesh(context, fp, frame=frame_num)
        elif file_format.upper() == "PLY":
            obj = load_ply_as_mesh(context, fp, frame=frame_num)
        else:
            obj = load_vtk_as_mesh(context, fp, frame=frame_num)

        if obj is None:
            continue

        # Hide on all frames, show only on its own frame
        obj.hide_viewport = True
        obj.hide_render = True
        obj.keyframe_insert(data_path="hide_viewport", frame=0)
        obj.keyframe_insert(data_path="hide_render", frame=0)

        obj.hide_viewport = False
        obj.hide_render = False
        obj.keyframe_insert(data_path="hide_viewport", frame=frame_num)
        obj.keyframe_insert(data_path="hide_render", frame=frame_num)

        if idx + 1 < len(files):
            obj.hide_viewport = True
            obj.hide_render = True
            obj.keyframe_insert(data_path="hide_viewport", frame=frame_num + 1)
            obj.keyframe_insert(data_path="hide_render", frame=frame_num + 1)

        objects.append(obj)

    return objects

