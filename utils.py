def _read_vtk_binary_structured_points(filepath: str):
    """Parse a binary VTK STRUCTURED_POINTS file.

    Returns ``(data_array, (Nx, Ny, Nz))`` where *data_array* is a numpy
    array shaped ``(Nz, Ny, Nx)`` (z-major, matching VTK storage order).

    Only the first scalar field is read (SCALARS keyword).  The caller is
    responsible for selecting the correct file (e.g. ``rho_*.vtk``).
    """
    import numpy as np

    dims = (1, 1, 1)
    data_type_str = "float"
    n_components = 1
    in_point_data = False

    with open(filepath, "rb") as f:
        # --- ASCII header ---
        header_bytes = []
        while True:
            line_bytes = f.readline()
            if not line_bytes:
                break
            header_bytes.append(line_bytes)
            line = line_bytes.decode("ascii", errors="replace").strip()
            parts = line.split()
            if not parts:
                continue
            kw = parts[0].upper()
            if kw == "DIMENSIONS" and len(parts) >= 4:
                dims = (int(parts[1]), int(parts[2]), int(parts[3]))
            elif kw == "POINT_DATA":
                in_point_data = True
            elif in_point_data and kw == "SCALARS" and len(parts) >= 3:
                data_type_str = parts[2].lower()
                n_components = int(parts[3]) if len(parts) > 3 else 1
            elif kw == "LOOKUP_TABLE":
                # Binary data starts immediately after this line
                break

        # --- Binary payload ---
        dtype_map = {
            "float":         ">f4",
            "double":        ">f8",
            "int":           ">i4",
            "unsigned_int":  ">u4",
            "short":         ">i2",
            "unsigned_short": ">u2",
            "char":          ">i1",
            "unsigned_char": ">u1",
        }
        np_dtype = dtype_map.get(data_type_str, ">f4")
        raw = f.read()

    Nx, Ny, Nz = dims
    arr = np.frombuffer(raw, dtype=np_dtype)

    if n_components > 1:
        arr = arr.reshape(Nz, Ny, Nx, n_components)
    else:
        arr = arr.reshape(Nz, Ny, Nx)

    return arr, dims


def vtk_binary_to_vdb(vtk_path: str, vdb_path: str, threshold: float = 0.005) -> str:
    """Convert a binary VTK STRUCTURED_POINTS density field to OpenVDB.

    *vtk_path*  – path to the ``rho_*.vtk`` file written by FluidX3D
    *vdb_path*  – output ``.vdb`` file path
    *threshold* – density values below this are treated as empty (gas/solid)

    Requires ``pyopenvdb`` (bundled with Blender 3.0+ OpenVDB builds) and
    ``numpy``.  Raises RuntimeError if either dependency is unavailable.
    """
    try:
        import pyopenvdb as vdb  # type: ignore
    except ImportError as exc:
        raise RuntimeError(
            "pyopenvdb is not available in this Blender build.  "
            "Install Blender with OpenVDB support or convert VTK files manually."
        ) from exc

    import numpy as np

    arr, dims = _read_vtk_binary_structured_points(vtk_path)
    Nx, Ny, Nz = dims

    # arr is (Nz, Ny, Nx) float32 – convert to float32 and clamp
    density = arr.astype(np.float32)
    density = np.clip(density, 0.0, None)

    # Create an OpenVDB fog volume grid
    grid = vdb.FloatGrid()
    grid.name = "density"
    grid.gridClass = vdb.GridClass.FOG_VOLUME
    grid.background = 0.0

    # pyopenvdb.copyFromArray expects the array indexed as arr[x, y, z].
    # Our VTK data is (Nz, Ny, Nx) in memory (z outer, x inner), which is
    # equivalent to arr[z, y, x].  Transposing gives arr[x, y, z].
    grid.copyFromArray(density.T, ijk_min=(0, 0, 0), tolerance=threshold)

    os.makedirs(os.path.dirname(vdb_path) or ".", exist_ok=True)
    vdb.write(vdb_path, grids=[grid])
    return vdb_path


def convert_vtk_cache_to_vdb(vtk_dir: str, vdb_dir: str) -> list:
    """Convert all ``rho_*.vtk`` files in *vtk_dir* to ``.vdb`` files in *vdb_dir*.

    Files are sorted by name so that frame order is preserved.
    Returns the list of generated ``.vdb`` file paths.
    """
    import glob as _glob

    vtk_files = sorted(_glob.glob(os.path.join(vtk_dir, "rho*.vtk")))
    if not vtk_files:
        log.warning("No rho*.vtk files found in %s", vtk_dir)
        return []

    os.makedirs(vdb_dir, exist_ok=True)
    vdb_paths = []
    for vtk_path in vtk_files:
        base = os.path.splitext(os.path.basename(vtk_path))[0]
        vdb_path = os.path.join(vdb_dir, base + ".vdb")
        try:
            vtk_binary_to_vdb(vtk_path, vdb_path)
            vdb_paths.append(vdb_path)
            log.info("Converted %s → %s", vtk_path, vdb_path)
        except Exception as exc:
            log.error("Failed to convert %s: %s", vtk_path, exc)

    return vdb_paths


