"""Utility helpers for the FluidX_Blender addon.

Covers:
  - FluidX3D C++ setup-file generation
  - Loading OBJ / PLY surface meshes produced by FluidX3D
  - Loading legacy ASCII/binary VTK files (POLYDATA and UNSTRUCTURED_GRID)
    as Blender mesh objects or OpenVDB-backed volume objects
"""

import os
import logging
from typing import Optional

log = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# C++ setup-file generation
# ---------------------------------------------------------------------------

_SETUP_TEMPLATE = """\
// FluidX3D simulation setup – generated by FluidX_Blender
// Place this file as  <FluidX3D>/src/setup.cpp  and recompile.

#include "src/lbm.hpp"
#include "src/units.hpp"

void main_setup() {{
    // ---- Domain ----
    const uint Nx = {Nx}u;
    const uint Ny = {Ny}u;
    const uint Nz = {Nz}u;

    // ---- Physics ----
    // kinematic viscosity (lattice units); Re = u_max * L / nu
    const float nu = {nu}f;
    // fluid density (lattice units)
    const float rho = {rho}f;

    // ---- Boundary conditions ----
    // Each face: P=periodic  W=no-slip wall  F=free-slip  V=velocity
    const uchar  boundary_x_neg = {bc_x_neg};
    const uchar  boundary_x_pos = {bc_x_pos};
    const uchar  boundary_y_neg = {bc_y_neg};
    const uchar  boundary_y_pos = {bc_y_pos};
    const uchar  boundary_z_neg = {bc_z_neg};
    const uchar  boundary_z_pos = {bc_z_pos};

    LBM lbm(Nx, Ny, Nz, nu);

    // ---- Initial conditions ----
    // Set the initial density and velocity fields here.
    const uint n = lbm.get_N();
    for (uint i = 0u; i < n; i++) {{
        lbm.rho[i] = rho;
        lbm.u.x[i] = 0.0f;
        lbm.u.y[i] = 0.0f;
        lbm.u.z[i] = 0.0f;
        lbm.flags[i] = TYPE_F;  // fluid
    }}

    // ---- Boundary flags ----
    lbm.flags.write_to_device();
    lbm.rho.write_to_device();
    lbm.u.write_to_device();

    // ---- Output settings ----
    lbm.graphics.set_camera_free(
        float3(0.5f * Nx, -2.0f * Ny, 0.5f * Nz),
        float3(0.5f * Nx, 0.5f * Ny, 0.5f * Nz)
    );

    // ---- Run ----
    while (lbm.get_t() <= {time_steps}u) {{
        if (lbm.get_t() % {output_every}u == 0u) {{
#ifdef EXPORT_VTK
            lbm.vtk_export_lbm(lbm.get_t());
#endif
#ifdef EXPORT_MESH
            lbm.vtk_export_surface(lbm.get_t());
#endif
        }}
        lbm.run(1u, {time_steps}u);
    }}
}}
"""

# Map human-readable boundary names to FluidX3D flag characters
_BC_FLAG = {
    "PERIODIC": "TYPE_P",
    "WALL": "TYPE_W",
    "FREE_SLIP": "TYPE_S",
    "VELOCITY": "TYPE_V",
}


def generate_setup_code(props) -> str:
    """Return a FluidX3D setup.cpp string built from *props*."""

    def bc(name: str) -> str:
        return _BC_FLAG.get(name, "TYPE_W")

    return _SETUP_TEMPLATE.format(
        Nx=props.domain_res_x,
        Ny=props.domain_res_y,
        Nz=props.domain_res_z,
        nu=props.viscosity,
        rho=props.density,
        bc_x_neg=bc(props.bc_x_neg),
        bc_x_pos=bc(props.bc_x_pos),
        bc_y_neg=bc(props.bc_y_neg),
        bc_y_pos=bc(props.bc_y_pos),
        bc_z_neg=bc(props.bc_z_neg),
        bc_z_pos=bc(props.bc_z_pos),
        time_steps=props.time_steps,
        output_every=props.output_every,
    )


def write_setup_file(props, output_dir: str) -> str:
    """Write the generated setup.cpp to *output_dir* and return its path."""
    os.makedirs(output_dir, exist_ok=True)
    path = os.path.join(output_dir, "setup.cpp")
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(generate_setup_code(props))
    return path


